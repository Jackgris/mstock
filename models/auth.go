// This var go to verify if the data needed for create token has valid characters
var re = regexp.MustCompile("[\\w!#$%&'*+/=?^_`{|}~-]+(?:\\.[\\w!#$%&'*+/=?^_`{|}~-]+)*@(?:[\\w](?:[\\w-]*[\\w])?\\.)+[a-zA-Z0-9](?:[\\w-]*[\\w])?")

// With this data we could authentic the user
type Token struct {
	Hash   string
	Expire time.Time
}

// Create and return a object Token, this need one o more strings for generate
// a hash
func GenerateToken(d ...interface{}) (Token, error) {
	var data string
	var ok bool
	if len(d) > 2 {
		data = ""
		for argNum := 0; argNum < len(d); argNum++ {
			arg, ok := d[argNum].(string)
			if !ok {
				return Token{}, errors.New("can't parse the list data for create hash token")
			}
			data += arg
		}
	} else {
		data, ok = d[0].(string)
		if !ok {
			return Token{}, errors.New("invalid data, you can not create the token")
		}
	}

	ok = re.MatchString(data)
	if !ok {
		return Token{}, errors.New("invalid data, you can not create the token")
	}

	expire := time.Now().Add(Tokenduration)
	token := jwt.New(jwt.SigningMethodHS256)
	token.Claims["exp"] = expire
	token.Claims["iat"] = time.Now().Unix()
	token.Claims["sub"] = data
	hash, err := token.SignedString([]byte(Privatekey))
	t := Token{}
	if err != nil {
		return t, errors.New("Error generating hash token" + err.Error())
	}
	t.Hash = hash
	t.Expire = expire
	return t, nil
}

// Get a object token from a string hash that represent the token
func ParseToken(token string) (*jwt.Token, error) {
	t, err := jwt.Parse(token, func(token *jwt.Token) (interface{}, error) {
		// Validate the alg is what you expect:
		if _, ok := token.Method.(*jwt.SigningMethodHMAC); !ok {
			return nil, fmt.Errorf("Unexpected signing method: %v", token.Header["alg"])
		}
		return []byte(Privatekey), nil
	})
	return t, err
}

